def classify(name: str):
    """根据 ETF 名称返回 (细分行业, 大类)。"""
    if not name:
        return ("未分类", "其他")
    for keywords, sub, big in RULES:
        for kw in keywords:
            if kw in name:
                return (sub, big)
    return ("未分类", "其他")


